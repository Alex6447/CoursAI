import type { ReactNode } from 'react'
import PageHeader from '../components/PageHeader'

function Section({
  code,
  title,
  children
}: {
  code: string
  title: string
  children: ReactNode
}): JSX.Element {
  return (
    <section className="mb-8">
      <div className="mb-3 flex items-baseline gap-3 border-b border-line pb-2">
        <span className="font-mono text-sm text-muted">{code}</span>
        <h2 className="font-mono text-lg text-text">{title}</h2>
      </div>
      <div className="space-y-3 text-sm leading-relaxed text-text/90">{children}</div>
    </section>
  )
}

function Bullets({ items }: { items: ReactNode[] }): JSX.Element {
  return (
    <ul className="space-y-1.5">
      {items.map((t, i) => (
        <li key={i} className="flex gap-2.5">
          <span className="mt-[7px] h-1 w-1 shrink-0 rounded-full bg-accent-dim" />
          <span>{t}</span>
        </li>
      ))}
    </ul>
  )
}

function Pill({ tone, children }: { tone: string; children: ReactNode }): JSX.Element {
  return (
    <span className={`rounded border px-1.5 py-0.5 font-mono text-[11px] ${tone}`}>{children}</span>
  )
}

function Callout({
  tone,
  label,
  children
}: {
  tone: string
  label: string
  children: ReactNode
}): JSX.Element {
  return (
    <div className={`rounded border border-line border-l-2 ${tone} bg-panel px-4 py-3`}>
      <div className="label-mono mb-1">{label}</div>
      <div className="text-sm leading-relaxed text-text/90">{children}</div>
    </div>
  )
}

// Подсказка для inline-кода/названий полей.
function C({ children }: { children: ReactNode }): JSX.Element {
  return (
    <code className="rounded bg-panel-2 px-1.5 py-0.5 font-mono text-[13px] text-accent">
      {children}
    </code>
  )
}

